				<p>O tratamento do câncer pode ser feito através e cirurgia, quimioterapia, radioterapia ou transplante de medula óssea. Em muitos casos, é necessário combinar mais de uma modalidade.</p>
				<p><strong>CIRURGIA:</strong>
				O ato cirúrgico pode ter finalidade curativa, sobretudo quando há detecção precoce do tumor e é possível sua retirada total; ou finalidade paliativa, quando o objetivo é de reduzir a quantidade de células tumorais ou de controlar sintomas que comprometam a qualidade da sobrevivência do paciente</p>
				<p>A cirurgia oncológica também é uma forma de avaliar a extensão da doença. Ou seja, em alguns casos, o estadiamento do câncer só é possível de ser certificado durante o ato cirúrgico</p>
				<p><strong>QUIMIOTERAPIA:</strong>
				É um tipo de tratamento em que se utilizam medicamentos para combater o câncer. Estes medicamentos se misturam com o sangue (via oral, intravenosa, intramuscular, subcutânea, intratecal e tópica) e são levados a todas as partes do corpo, destruindo as células doentes que estão formando o tumor e impedindo, também, que se espalhem</p>
				<p><strong>RADIOTERAPIA:</strong>
				A radioterapia é um tratamento no qual se utilizam radiações ionizantes (raio-x, por exemplo), que são um tipo de energia para destruir ou impedir que as células do tumor aumentem. Essas radiações não são vistas e durante a aplicação o paciente não sente nada.</p>
				<p><strong>TRANSPLANTE DE MEDULA ÓSSEA:</strong>
				O transplante de medula óssea é um tipo de tratamento proposto para algumas doenças que afetam as células do sangue, como as leucemias e os linfomas e consiste na substituição de uma medula óssea doente ou deficitária por células normais de medula óssea, com o objetivo de reconstituição de uma medula saudável. O transplante pode ser autogênico, quando a medula vem do próprio paciente. No transplante alogênico a medula vem de um doador. O transplante também pode ser feito a partir de células precursoras de medula óssea, obtidas do sangue circulante de um doador ou do sangue de cordão umbilical. <br />(Fonte: INCA, 2019)</p>
